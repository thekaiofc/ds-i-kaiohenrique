package Exercícios;

public class Ex01 {

    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println("Este número é par: " + i);
            } else {
                System.out.println("Este número é impar: " + i);
            }
        }
    }
}
